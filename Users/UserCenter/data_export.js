function download_selfstudy_all_data() {
    let startDate = $("#selfstudy-start-date").val();
    let endDate = $("#selfstudy-end-date").val();

    $.post(
        "/Ajax/DataManager/download_selfstudy_all_data",
        {
            startDate: startDate,
            endDate: endDate
        },
        function (data, status) {
            if (status === "success") {
                let returnCode = data['code'];
                if (returnCode === 400) {
                    swal({
                        title: "提供的数据错误，请联系管理员",
                        text: data['message'],
                        icon: "error",
                    });
                }
                else if (returnCode === 401) {
                    swal({
                        title: "权限错误",
                        text: "仅队长和数据组组长与组员可下载。",
                        icon: "error",
                    });
                }
                else if (returnCode === 404) {
                    swal({
                        title: "功能不存在，请联系管理员",
                        icon: "warning",
                    });
                }
                else if (returnCode === 417) {
                    swal({
                        title: "功能错误，请联系管理员",
                        icon: "warning",
                    });
                }
                else if (returnCode === 498) {
                    swal({
                        title: "数据库异常，请联系管理员",
                        icon: "warning",
                    });
                }
                else if (returnCode === 499) {
                    swal({
                        title: "功能维护中，暂不允许下载早自习检查数据",
                        icon: "warning",
                    });
                }
                else if (returnCode === 200 || returnCode === 301) {
                    //状态码301，提醒转移函数
                    if (returnCode === 301) { window.console.log('下载早自习检查数据函数移至新位置'); }
                    //状态码200，处理data
                    window.open(data["data"]);
                }
            }
            else
                alert("请检查网络状况。");
        })
}

